public class Kieu_du_lieu {
    public static void main(String[] args) {
        int number = 200;
        byte number_byte = 50;
        float number_float = 10.5f;
        double number_double = 11.2;
        boolean number_boolean = false;
        System.out.println("Kieu du lieu int: " + number);
        System.out.println("Kieu du lieu byte: " + number_byte);
        System.out.println("Kieu du lieu float: " + number_float);
        System.out.println("Kieu du lieu double: " + number_double);
        System.out.println("Kieu du lieu boolean: " + number_boolean);
    }
}
